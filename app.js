function sumar(num1, num2) {
    let suma = (num1 + num2);
    let resultado = "El resultado de la suma es " + suma;
    return resultado;
}

console.log(sumar(6, 4));

function restar(num1, num2) {
    let resta = (num1 - num2);
    let resultado = "El resultado de la resta es " + resta;
    return resultado;
}

console.log(restar(6, 4));